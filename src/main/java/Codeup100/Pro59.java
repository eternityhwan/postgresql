package Codeup100;

import java.util.Scanner;

public class Pro59 {

        public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);

            int a = sc.nextInt();
            int b = sc.nextInt();
            System.out.println(a|b);

            // 비트단위로 연산한 후 그 결과를 정수로 출력
            // 연산자 |를 쓰면됨. 버티컬바바
            // 이러한 비트단위 연산은 빠른 계산이 필요한 그래픽처리에서도 효과적으로 사용된다.
       }
    }

