package Codeup100;

import java.util.Scanner;

public class Pro60 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int a = sc.nextInt();
        int b = sc.nextInt();
        System.out.println(a^b);

        // 비트단위로 연산한 후 그 결과를 정수로 출력
        // 연산자 ^를 쓰면됨. 서컴플렉스/카릿
        // 수학에서 거듭제곱을 나타내는 기호와 모양만같음
    }
}
