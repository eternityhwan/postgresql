package method.exercise;

public class HowToUseArray {
    public static void main(String[] args) {
        // 배열 쓰는 법
        // 원시적

        int[] student1 = {100, 99, 98};
        int[] student2 = {99, 98, 97};
        int[] student3 = {99, 98, 97};
        int[] student4 = {99, 98, 97};
        int[] student5 = {99, 98, 97};
        // 학생 5명 각각의 총점

        System.out.println(student1[0] + student1[1] + student1[2]);
        System.out.println(student2[0] + student2[1] + student2[2]);
        System.out.println(student3[0] + student3[1] + student3[2]);
        System.out.println(student4[0] + student4[1] + student4[2]);
        System.out.println(student5[0] + student1[1] + student1[2]);
    }
}
